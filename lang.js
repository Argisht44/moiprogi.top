function setLanguage(lang) {
  localStorage.setItem("language", lang);
  applyTranslations();
}

function detectLanguage() {
  const savedLang = localStorage.getItem("language");
  if (savedLang) {
    setLanguage(savedLang);
    return;
  }
  const browserLang = navigator.language.split('-')[0]; // Например, "en" из "en-US"
  const supportedLangs = ['ru', 'uk', 'en', 'de'];
  const lang = supportedLangs.includes(browserLang) ? browserLang : 'ru';
  setLanguage(lang);
}

function applyTranslations() {
  const lang = localStorage.getItem("language") || "ru";
  document.querySelectorAll("[id]").forEach((element) => {
    const key = element.id;
    if (translations[lang][key]) {
      element.textContent = translations[lang][key];
    }
  });
  document.querySelectorAll("[data-lang-key]").forEach((element) => {
    const key = element.getAttribute("data-lang-key");
    if (translations[lang][key]) {
      if (element.tagName === "META") {
        element.setAttribute("content", translations[lang][key]);
      } else if (element.tagName === "TITLE") {
        element.textContent = translations[lang][key];
      } else {
        element.textContent = translations[lang][key];
      }
    }
  });
  document.querySelectorAll("[data-lang-alt]").forEach((element) => {
    const key = element.getAttribute("data-lang-alt");
    if (translations[lang][key]) {
      element.setAttribute("alt", translations[lang][key]);
    }
  });
  document.querySelectorAll("[data-lang-title]").forEach((element) => {
    const key = element.getAttribute("data-lang-title");
    if (translations[lang][key]) {
      element.setAttribute("title", translations[lang][key]);
    }
  });
  document.documentElement.lang = lang;
}

document.addEventListener("DOMContentLoaded", detectLanguage);